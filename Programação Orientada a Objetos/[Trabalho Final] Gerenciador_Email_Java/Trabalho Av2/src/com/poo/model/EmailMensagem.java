package com.poo.model;

public class EmailMensagem {
	
	String email_origem,data_envio,assunto,mensagem;

	public String getEmail_origem() {
		return email_origem;
	}

	public void setEmail_origem(String email_origem) {
		this.email_origem = email_origem;
	}

	public String getData_envio() {
		return data_envio;
	}

	public void setData_envio(String data_envio) {
		this.data_envio = data_envio;
	}

	public String getAssunto() {
		return assunto;
	}

	public void setAssunto(String assunto) {
		this.assunto = assunto;
	}

	public String getMensagem() {
		return mensagem;
	}

	public void setMensagem(String mensagem) {
		this.mensagem = mensagem;
	}
	

}
